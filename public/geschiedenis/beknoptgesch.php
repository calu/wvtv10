  <p class='indent'>1907 HECTOEN suggereert dat de veiligheid van transfusies mogelijk 
  kan verbeteren door het invoeren van een kruisproef tussen het bloed van de 
  pati&euml;nt en dat van de donoren om incompatibele mengsels uit te sluiten. Reuben 
  OTTENBERG 
  (New York) voert de eerste bloedtransfusie uit na een het uitvoeren van een 
  bloedgroepbepaling en een kruisproef.</p>
  
  <p class='indent'>[Stijn STREUVELS publiceert De Vlasschaard. Priester DAENS sterft.]</p>
  
  <p class='indent'>1908 Alexis CARREL (1873-1944) zoekt een uitweg om stolling te voorkomen 
  door een arterie van de donor vast te naaien aan de ader van de ontvanger. Deze 
  'ader-tot-ader' of 'directe' methode, toen ook gekend als anastomose, wordt 
  door verschillende geneesheren toegepast. De procedure wordt uiteindelijk verlaten 
  daar zij meestal niet haalbaar is. </p>
  
  <p class='indent'>[Een KB erkent de Belgische soevereiniteit over Kongo. Men denkt er aan een Ministerie 
  van Koloni&euml;n op te richten.]</p>
  
