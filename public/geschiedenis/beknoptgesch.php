  dat O-bloed aan pati&euml;nten van om het even welke bloedgroep veilig kan worden 
  toegediend en dat pati&euml;nten met de bloedgroep AB bloed kunnen krijgen van elke 
  bloedgroep. De termen 'universele donor' en 'universele receptor' doen hun 
  intrede.</p>
  
  <p class='indent'>[Jules DESTREE publiceert zijn brief <i>Sire, er zijn geen Belgen meer</i>.]</p>
  
  <p class='indent'>1913 Arthur Ronald KIMPTON (&deg;1881) en James Howard BROWN (&deg;1884) beschrijven 
