  <p class='indent'>1667 Jean Baptiste DENIS (1625-1704), een filosoof en wiskundige (geen geneesheer!), 
  voert op 15 juni, 
  met succes, een transfusie uit met ongeveer 350 ml (12 oz) schapenbloed bij 
  een jonge geesteszieke. Hij schreef ook een brief over 'een nieuwe manier om 
  door bloedtransfusie verschillende ziekten te genezen'. In ditzelfde jaar voert 
  Richard LOWER 
  (1631-1691) met de hulp van Edmund KING> (1629-1709) een gelijkaardig experiment uit 
  bij een man. Het is de eerste bloedtransfusie bij een mens in Engeland. Hierdoor 
  ontstaat discussie over wie de methode voor het eerst toepaste. </p>
  
  <p class='indent'>[Robert BOYLE demonstreert voor de 
  Royal Society dat kunstmatige beademing een dier in leven kan houden.] [In Brussel 
  maakt een pestepidemie 4.066 slachtoffers.]</p>
  
  <p class='indent'>1668 Transfusies van dierenbloed bij de mens 
  worden bij wet verboden, wat een einde stelt aan de vooruitgang op het gebied 
  van de bloedtransfusie gedurende een periode van 150 jaar. Antoni van LEEUWENHOEK> beschrijft de rode 
  bloedcellen.</p>
  
  <p class='indent'>[Tijdens de herfst verspreidt de pest zich over gans Europa.] </p>
  
  <p class='indent'>1818 James BLUNDELL (1790-1877 of 78?) ontwikkelde een spuit waarmee hij 
  honden transfuseerde. </p>
  <p class='indent'>[In Parijs wordt de eerste bestuurbare loopfiets tentoongesteld.]</p>
  
  <p class='indent'>1819 BLUNDELL publiceert een poging om, door het intraveneus inspuiten 
  van bloed, het leven te verlengen bij een pati&euml;nt met aanhoudend braken. De 
  man kreeg bloed van verschillende donoren met behulp van de door hem uitgevonden 
  spuit maar stierf 56 uur na de transfusie.</p>
  
  <p class='indent'>[Ren&eacute; LAENNEC vindt de stethoscoop uit.]</p>
  
  <p class='indent'>1821 Jean Louis PR&Eacute;VOST (1790-1850) en Jean Baptiste ANDR&Eacute; (1800-1884) 
  gebruiken voor het eerst met succes 
  gedefibrineerd bloed voor transfusie bij dieren, in een prille poging om stolling 
  tijdens transfusie te voorkomen.</p>
  
  <p class='indent'>[Samuel HAHNEMANN introduceert de 
  homeopathie.] [Charles BELL (&deg; 1744) beschrijft voor het eerst de facialisparese.]</p>
  
  <p class='indent'>1829 BLUNDELL voert de eerste bloedtransfusie uit van mens tot mens 
  waarbij de pati&euml;nt overleefde, naar aanleiding van een bloeding in het post 
  partum. Hij gebruikte de echtgenoot als donor, nam ongeveer 125 gram bloed af 
  uit zijn arm en transfuseerde de vrouw met behulp van zijn spuit. Hij legde 
  de meest fundamentele punten vast in verband met bloedtransfusie, zoals de incompatibiliteit 
  van interspecies transfusies en de werkwijze voor de indirecte transfusie. Door 
  zijn beschrijving van een tiental gevallen over een periode van een decennium 
  herleefde de interesse voor transfusie na een dode periode van anderhalve eeuw. 
  </p>
  
  <p class='indent'>[Gabriel ANDRAL gebruikt voor het 
  eerst de term 'anemie'. Louis BRAILLE ontwikkelt zijn alfabet voor blinden.]</p>
  
  <p class='indent'>1840 Samuel Armstrong LANE 
  (St. George's School, Londen) rapporteert een succesvolle behandeling van hemofilie 
  dank zij een bloedtransfusie (de <i>Lancet</i>).</p>
  
  <p class='indent'>[In Engeland verschijnt de eerste postzegel.] [Het lichaam van Napoleon wordt bijgezet in 
  het H&ocirc;tel des Invalides.]</p>
  
  <p class='indent'>1867 Leonard LANDOIS (1837-1905) en Pierre Cyprien OR&Eacute;> (1828-1890) experimenteren 
  met bloedtransfusie. Or&eacute; voerde m&eacute;&eacute;r dan 300 transfusies uit bij mensen en gaf de 
  voorkeur aan transfusies met lamsbloed, dat volgens hem zonder gevaar was: de vast gestelde incidenten 
  waren goedaardig en van voorbijgaande aard.</p>
  
  <p class='indent'>[Lister publiceert in de <i>Lancet</i> zijn artikel over antisepsis in de chirurgie.] 
  [ Karl Marx publiceert <i>Das Kapital</i>.]</p>
  
  <p class='indent'>1869 John Braxton HICKS (1823-1897) publiceert enkele gevallen van bloedtransfusie 
  en bespreekt het gebruik van natriumfosfaat als anticoagulans.</p>
  
  <p class='indent'>[Dmitri MENDELJEW stelt het periodiek systeem der elementen voor.] 
  [Feestelijke opening van het Suez-kanaal.]</p>
  
  <p class='indent'>1873 James Hobson AVELING (1828-1892) bespreekt zeven gevallen van directe transfusie en 
  de manier waarop hij hierbij tewerk gaat. Hij maakte gebruik van een draagbaar transfusietoestel.</p>
  
  <p class='indent'>[In Vlaanderen wordt de rechtspraak in het Nederlands mogelijk.] [David LIVINGSTONE sterft.]</p>
  
  <p class='indent'>1874 Leonard LANDOIS (1837-1902) beschrijft het hemolyserend effect van bloedserum 
  na transfusie ervan bij een andere species.</p>
  
  <p class='indent'>[De impressionisten houden hun eerste tentoonstelling in Parijs.]</p>
  
  <p class='indent'>1900 Karl LANDSTEINER (1868-1943) maakt zijn ontdekking bekend van de bloedgroepen 
  die hij aanvankelijk indeelt in drie groepen: A, B en O. Hiervoor krijgt hij 
  in 1930 de Nobelprijs voor geneeskunde.</p>
  
  <p class='indent'>[Na Anatole Marie Emile CHAUFFARD in 1899, beschrijft nu ook Oskar MINKOWSKI de congenitale 
