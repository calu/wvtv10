  <p class='indent'>1628 <b>William HARVEY</b> (1578-1657), <i>Exercitatio anatomica de motu cordis et sanguinis in 
  animalibus</i>, Frankfurt. Dit boek, uitgegeven door W. Fitzer, betekent een essentieel 
  keerpunt in de geschiedenis van de geneeskunde. Hij bewijst en beschrijft de 
  bloedsomloop, hij heeft over 'systolische ejectie' en 'hartdebiet' en wijst 
  op een mogelijk hemodynamisch verschil in de twee circulaties.</p>
  
  <p class='indent'>Giovanni Dionisio COLLE da Belluno (1558-1630 of 31?) geeft op p. 170 van zijn 
      <i>Methodus facile parandi iucunda tuta et nova medicamenta</i> (Veneti&euml;) de eerste 
      ondubbelzinnige beschrijving van een bloedtransfusie en probeert bloedtransfusies tussen dieren.</p>
      
