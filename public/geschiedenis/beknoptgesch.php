<h1>Beknopte geschiedenis van de transfusie</h1>

<div>
   <p class='indent'>Chronologisch overzicht van de voornaamste mijlpalen in de geschiedenis 
  van de bloedtransfusie. Voor een breder perspectief staan tussen vierkante haken 
  gebeurtenissen van buiten de transfusie.</p>
  
  <p class='indent'>1543 <b>Andreas VESALIUS</b> (1514-1564) legde met zijn <i>De humani corporis 
  fabrica</i> (Oporinus, Bazel) de basis voor de belangrijke fysiologische concepten die zullen volgen. Zijn 
  anatomische studies waren gebaseerd op eigen dissecties van mensenlijken en op een nauwgezette waarneming en precieze tekeningen. De afbeeldingen van zijn 
  dissectiewerk boden accurate informatie over de structuur en de organisatie van het bloedvatenstelsel. Hij bespreekt het merg in de beenholte en de kleine 
  holten van het spongieus bot, maar situeert de bloedvorming in de lever.</p>
  
