  <p class='indent'>1914 De Belg Albert HUSTIN (1882-1967) en daarna Luis AGOTE (1869-1954) uit Argentini&euml; 
  en Richard LEWISOHN (1875-1962), een chirurg uit New York, gebruiken natriumcitraat als anticoagulans voor 
  transfusiebloed. 
  HUSTIN combineerde het natriumcitraat met glucose en stelde 
  die formule voor in <i>Note sur une nouvelle m&eacute;thode de transfusion</i>, Bull. 
  Soc. roy. Sci. m&eacute;d. Brux., 1914, 72, 104-11. Hoewel deze belangrijke ontwikkeling 
  toelaat om transfusies pas uit te voeren na de afname bij de donor - overschakeling 
  van directe naar indirecte transfusie - duurt het 10 jaar vooraleer het gebruik 
  van natriumcitraat algemeen aanvaard wordt.</p>
  
  <p class='indent'>[John Jacob ABEL en Leonhard George 
  ROWNTREE concipi&euml;ren de kunstmatige nier. Sir Frederick SODDY ontdekt de isotopen. 
