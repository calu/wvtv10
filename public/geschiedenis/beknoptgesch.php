  de twee gekende soorten bloedvaten, met elkaar verbinden. Hij vervolledigt zo 
  het werk van Harvey.</p>
  
  <p class='indent'>[K.V. SCHNEIDER uit Wittenberg stelt dat neusloop een aandoening is van het neusslijmvlies 
  en geen hersenaandoening zoals Galenus dacht.]</p>
  
  <p class='indent'>1665 Richard LOWER (1631-1691) voert een geslaagde bloedtransfusie uit 
  van hond tot hond. Vier jaar later zal hij aantonen dat het bloed van kleur 
  verandert in de longen.</p>
  
  <p class='indent'>[Premi&egrave;re van Moli&egrave;re's <i>Le m&eacute;decin malgr&eacute; lui</i>.]</p>
